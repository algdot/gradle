public class Order {

    /**
     *  订单数量
     */
    private Integer num;
    /**
     * 金额
     */
    private Double amount;

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }


    public Double getAmount() {
        return amount;
    }

    public void setAmount(Double amount) {
        this.amount = amount;
    }
}
